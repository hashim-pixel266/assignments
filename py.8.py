# 4ï¸âƒ£ Define a mixed list and convert strings to integers using a loop
mixed = ["5", 8, 10.5, True]
converted = []
for item in mixed:
    if isinstance(item, str):
        converted.append(int(item))
    else:
        converted.append(item)
print("Converted list:", converted)

